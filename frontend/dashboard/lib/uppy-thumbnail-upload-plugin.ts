import Uppy, { BasePlugin } from "@uppy/core";
import imageCompression from "browser-image-compression";
import { applyDefaultWatermark, applyWatermark } from "./watermark-utils";
import { getWatermarkConfig } from "./watermark-resolver";
import api from "./api-service";

/**
 * Custom Uppy plugin to upload three-tier optimized images to S3
 *
 * This plugin generates three versions of each image:
 * 1. Thumbnail (600px, maintain ratio): Generated using browser-image-compression for high quality
 * 2. BigThumb (1200px, maintain ratio): Uses browser-image-compression library (2x quality)
 * 3. Preview (2800px, maintain ratio): Uses browser-image-compression library (2x quality)
 *
 * All versions are converted to WebP format and uploaded to S3 using presigned URLs.
 *
 * Three-Tier Optimization Strategy (High Quality):
 * - Thumbnail (600px, maintain ratio, high quality 0.90-0.95): ~80-200KB
 *   - Use case: CMS grid views, admin panels, dashboard thumbnails
 *   - Generated by: browser-image-compression with high quality settings
 *   - 50% smaller than BigThumb (1200px) for optimal quality-to-size ratio
 *   - High quality prevents pixelation at 600px display size
 *
 * - BigThumb (1200px, maintain ratio, near-lossless quality 0.95-0.98): ~300-800KB
 *   - Use case: Masonry/responsive grid layouts in client gallery
 *   - Generated by: browser-image-compression with near-lossless quality settings
 *   - Near-lossless quality prevents any visible compression artifacts at 1200px display size
 *   - Perfect for beautiful gallery layouts on all devices
 *
 * - Preview (2800px, maintain ratio, adaptive quality 0.85-0.92): ~1.6-2.4MB
 *   - Use case: Full-screen quality preview for picky choosers
 *   - Generated by: browser-image-compression with adaptive quality
 *   - Quality adjusts based on original file size to hit target ranges
 *   - Near-lossless quality for professional review
 *   - 2x quality improvement from previous 1400px version
 *
 * Why this approach:
 * - browser-image-compression: Better than raw Canvas API (simpler, more reliable, handles EXIF)
 * - Consistent quality across all image sizes
 * - Custom thumbnail generation ensures high quality dashboard thumbnails
 */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
interface UppyFileWithMeta {
  meta?: {
    thumbnailPreview?: string;
    thumbnailBlob?: Blob;
    presignedData?: {
      previewUrl?: string;
      bigThumbUrl?: string;
      thumbnailUrl?: string;
    };
    [key: string]: unknown;
  };
  preview?: string;
  data?: File | Blob;
  type?: string;
  [key: string]: unknown;
}

// eslint-disable-next-line @typescript-eslint/no-explicit-any
export class ThumbnailUploadPlugin extends BasePlugin<any, any, any> {
  static VERSION = "1.0.0";

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  constructor(uppy: Uppy, opts: Record<string, unknown>) {
    super(uppy, opts);
    // eslint-disable-next-line @typescript-eslint/no-unsafe-member-access
    this.id = "thumbnail-upload";
    // eslint-disable-next-line @typescript-eslint/no-unsafe-member-access
    this.type = "modifier";
  }

  override install() {
    // Listen for upload completion to upload preview, bigThumb, and thumbnail
    // eslint-disable-next-line @typescript-eslint/no-unsafe-call, @typescript-eslint/no-unsafe-member-access
    (this.uppy as Uppy).on("upload-success", this.handleUploadSuccess.bind(this));
  }

  override uninstall() {
    // eslint-disable-next-line @typescript-eslint/no-unsafe-call, @typescript-eslint/no-unsafe-member-access, @typescript-eslint/unbound-method
    (this.uppy as Uppy).off("upload-success", this.handleUploadSuccess);
  }

  /**
   * Calculate adaptive quality based on original file size
   * Adjusts quality to better hit target file size ranges while maintaining quality priority
   *
   * Strategy:
   * - Very large originals (>20MB): More aggressive compression
   * - Large originals (10-20MB): Reduce quality to hit size targets
   * - Medium originals (2-10MB): Use standard quality
   * - Small originals (<2MB): Maintain quality (already small files)
   *
   * @param fileSizeMB - Original file size in MB
   * @param type - Image type: 'preview' or 'bigthumb'
   * @returns Quality value between 0.80-0.92
   */
  private calculateAdaptiveQuality(fileSizeMB: number, type: "preview" | "bigthumb"): number {
    if (type === "preview") {
      // Preview quality ranges: 0.85-0.92
      // Target: 0.8-1.2MB per preview
      if (fileSizeMB > 20) {
        // Very large originals: more aggressive compression
        return 0.85;
      } else if (fileSizeMB > 10) {
        // Large originals: reduce quality to hit size target
        return 0.88;
      } else if (fileSizeMB >= 2) {
        // Medium originals: use standard quality
        return 0.9;
      } else {
        // Small originals: maintain quality (already small files)
        return 0.92;
      }
    } else {
      // BigThumb quality ranges: 0.95-0.98 (near-lossless quality for 1200px images)
      // Maximum quality to prevent any visible compression artifacts at 1200px display size
      if (fileSizeMB > 20) {
        // Very large originals: very high quality
        return 0.95;
      } else if (fileSizeMB > 10) {
        // Large originals: very high quality
        return 0.96;
      } else if (fileSizeMB >= 2) {
        // Medium originals: near-lossless quality
        return 0.97;
      } else {
        // Small originals: maximum quality (near-lossless)
        return 0.98;
      }
    }
  }

  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  private async handleUploadSuccess(file: any) {
    const fileWithMeta = file as UppyFileWithMeta;
    // Only process image files
    if (!fileWithMeta.type?.startsWith("image/")) {
      return;
    }

    // Get presigned URLs from file metadata (set during getUploadParameters)
    const presignedData = fileWithMeta.meta?.presignedData;

    if (!presignedData?.previewUrl || !presignedData?.bigThumbUrl || !presignedData?.thumbnailUrl) {
      return;
    }

    try {
      // Calculate file size in MB for adaptive quality
      const fileSizeMB =
        fileWithMeta.data && fileWithMeta.data instanceof File
          ? fileWithMeta.data.size / (1024 * 1024)
          : 5; // Default to medium if size unknown

      // Get gallery ID from file metadata
      const galleryId = fileWithMeta.meta?.galleryId as string | undefined;
      
      // Fetch gallery and business info for watermark resolution
      let watermarkConfig: { url?: string; opacity?: number; isDefault: boolean } | null = null;
      if (galleryId) {
        try {
          const [gallery, businessInfo] = await Promise.all([
            api.galleries.get(galleryId).catch(() => null),
            api.auth.getBusinessInfo().catch(() => null),
          ]);
          watermarkConfig = getWatermarkConfig(gallery, businessInfo);
        } catch {
          // If watermark config fetch fails, continue without watermark
          watermarkConfig = null;
        }
      }

      // Generate all three versions in parallel for efficiency
      const [preview, bigThumb, thumbnailBlob] = await Promise.all([
        // Preview (2800px) - for full-screen quality viewing (adaptive quality, 2x quality)
        this.generatePreview(fileWithMeta, 2800),
        // BigThumb (1200px) - for masonry/responsive grid layouts (adaptive quality, 2x quality)
        this.generateBigThumb(fileWithMeta, 1200),
        // Thumbnail (600px) - 50% smaller than BigThumb for high quality dashboard thumbs
        this.generateThumbnail(fileWithMeta, fileSizeMB),
      ]);

      if (!preview || !bigThumb || !thumbnailBlob) {
        return;
      }

      // Apply watermarks if configured
      let finalPreview = preview;
      let finalBigThumb = bigThumb;
      let finalThumbnail = thumbnailBlob;

      if (watermarkConfig) {
        try {
          if (watermarkConfig.isDefault) {
            // Apply default "PREVIEW" watermark with multiply blend mode
            [finalPreview, finalBigThumb, finalThumbnail] = await Promise.all([
              applyDefaultWatermark(preview),
              applyDefaultWatermark(bigThumb),
              applyDefaultWatermark(thumbnailBlob),
            ]);
          } else if (watermarkConfig.url) {
            // Apply uploaded watermark (tiled pattern)
            const opacity = watermarkConfig.opacity ?? 0.7;
            
            [finalPreview, finalBigThumb, finalThumbnail] = await Promise.all([
              applyWatermark(preview, watermarkConfig.url, opacity),
              applyWatermark(bigThumb, watermarkConfig.url, opacity),
              applyWatermark(thumbnailBlob, watermarkConfig.url, opacity),
            ]);
          }
        } catch (error) {
          // If watermark application fails, use original images
          console.error("Failed to apply watermark:", error);
        }
      }

      // Upload all three versions to S3 in parallel
      await Promise.all([
        this.uploadToS3(presignedData.previewUrl, finalPreview, "image/webp"),
        this.uploadToS3(presignedData.bigThumbUrl, finalBigThumb, "image/webp"),
        this.uploadToS3(presignedData.thumbnailUrl, finalThumbnail, "image/webp"),
      ]);
    } catch (_error) {
      // Don't fail the upload if thumbnail upload fails
      // eslint-disable-next-line no-console
    }
  }

  /**
   * Generate thumbnail (600px, maintain ratio) from image using browser-image-compression
   * For dashboard thumbnails and CMS grid views
   *
   * Uses high quality settings (0.90-0.95) to prevent pixelation at 600px display size.
   * 50% smaller than BigThumb (1200px) for optimal quality-to-size ratio.
   * Maintains aspect ratio (not square cropped).
   *
   * @param fileSizeMB - Original file size in MB (for adaptive quality selection)
   */
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  private async generateThumbnail(file: any, fileSizeMB?: number): Promise<Blob | null> {
    const fileWithMeta = file as UppyFileWithMeta;
    if (!fileWithMeta.data || !(fileWithMeta.data instanceof File)) {
      return null;
    }

    try {
      // Calculate adaptive quality for thumbnails
      // Quality range: 0.90-0.95 (high quality for 600px thumbnails)
      // Prioritize quality over size - high quality ensures sharp thumbnails
      let quality: number;
      if (fileSizeMB && fileSizeMB > 20) {
        quality = 0.9;
      } else if (fileSizeMB && fileSizeMB > 10) {
        quality = 0.92;
      } else if (fileSizeMB && fileSizeMB >= 2) {
        quality = 0.93;
      } else {
        quality = 0.95;
      }

      // Resize to fit within 600px while maintaining aspect ratio
      // 600px is 50% smaller than BigThumb (1200px) for optimal quality
      const compressedFile = await imageCompression(fileWithMeta.data, {
        maxSizeMB: 2, // Increased limit for 600px thumbnails
        maxWidthOrHeight: 600, // Fit inside 600px, maintain aspect ratio
        useWebWorker: false,
        fileType: "image/webp",
        initialQuality: quality,
      });

      return compressedFile;
    } catch (_error) {
      return null;
    }
  }

  /**
   * Upload blob to S3 using presigned URL
   */
  private async uploadToS3(presignedUrl: string, blob: Blob, contentType: string): Promise<void> {
    const response = await fetch(presignedUrl, {
      method: "PUT",
      body: blob,
      headers: {
        "Content-Type": contentType,
      },
    });

    if (!response.ok) {
      throw new Error(`Failed to upload to S3: ${response.status} ${response.statusText}`);
    }
  }

  /**
   * Generate preview (2800px) from image using browser-image-compression
   * For full-screen quality viewing on desktop, 8K TV, and high-resolution displays
   *
   * Uses adaptive quality based on original file size to better hit target file size ranges
   * while maintaining quality priority for client selection.
   *
   * browser-image-compression is better than raw Canvas API because:
   * - Handles EXIF orientation automatically
   * - Better memory management
   * - Handles edge cases (very large images, etc.)
   * - Simpler API
   */
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  private async generatePreview(file: any, maxWidthOrHeight: number = 2800): Promise<Blob | null> {
    const fileWithMeta = file as UppyFileWithMeta;
    if (!fileWithMeta.data || !(fileWithMeta.data instanceof File)) {
      return null;
    }

    try {
      // Calculate adaptive quality based on original file size
      const fileSizeMB = fileWithMeta.data.size / (1024 * 1024);
      const quality = this.calculateAdaptiveQuality(fileSizeMB, "preview");

      // Use browser-image-compression to generate 2800px preview (2x quality improvement)
      // Optimized for full-screen quality viewing:
      // - 2800px: Perfect for desktop, 8K TV, and high-resolution displays (2x from 1400px)
      // - Adaptive quality (0.85-0.92): Adjusts based on original size to hit target
      //   Target: ~1.6-2.4MB per preview (adjusted for 2x size)
      // - Maintains aspect ratio (not square like thumbnail)
      const compressedFile = await imageCompression(fileWithMeta.data, {
        maxSizeMB: 10, // Increased limit for 2800px preview
        maxWidthOrHeight, // Fit inside 2800px, maintain aspect ratio
        useWebWorker: false, // Simpler, works everywhere
        fileType: "image/webp", // Convert to WebP
        initialQuality: quality, // Adaptive quality - excellent for professional review
      });

      return compressedFile;
    } catch (_error) {
      return null;
    }
  }

  /**
   * Generate BigThumb (1200px) from image using browser-image-compression
   * For masonry/responsive grid layouts in client gallery
   *
   * Uses near-lossless quality settings (0.95-0.98) to prevent any visible compression artifacts
   * at 1200px display size. Prioritizes quality over file size - maximum quality is essential
   * for 1200px images to look sharp and professional.
   *
   * This provides a middle tier between thumbnail and preview:
   * - Perfect for beautiful gallery layouts
   * - Maintains aspect ratio (not square like thumbnail)
   * - Near-lossless quality prevents any visible compression artifacts
   */
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  private async generateBigThumb(file: any, maxWidthOrHeight: number = 1200): Promise<Blob | null> {
    const fileWithMeta = file as UppyFileWithMeta;
    if (!fileWithMeta.data || !(fileWithMeta.data instanceof File)) {
      return null;
    }

    try {
      // Calculate adaptive quality based on original file size
      const fileSizeMB = fileWithMeta.data.size / (1024 * 1024);
      const quality = this.calculateAdaptiveQuality(fileSizeMB, "bigthumb");

      // Use browser-image-compression to generate 1200px BigThumb (near-lossless quality)
      // Optimized for masonry/responsive grid layouts:
      // - 1200px: Perfect for gallery grid views on all devices
      // - Adaptive quality (0.95-0.98): Near-lossless quality to prevent any visible artifacts
      // - Maintains aspect ratio (not square like thumbnail)
      // - No size constraint - quality is the only priority for 1200px images
      const compressedFile = await imageCompression(fileWithMeta.data, {
        // No maxSizeMB - let quality be the only constraint to ensure maximum quality
        maxWidthOrHeight, // Fit inside 1200px, maintain aspect ratio
        useWebWorker: false, // Simpler, works everywhere
        fileType: "image/webp", // Convert to WebP
        initialQuality: quality, // Near-lossless quality - prevents any visible compression artifacts
      });

      return compressedFile;
    } catch (_error) {
      return null;
    }
  }
}
